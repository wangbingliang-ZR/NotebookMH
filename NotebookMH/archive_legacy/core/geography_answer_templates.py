class GeographyAnswerTemplateLibrary:
    def __init__(self) -> None:
        self._templates = self._build_templates()
        self._index: Dict[str, AnswerTemplate] = {}
        for t in self._templates:
            self._index[t.template_id] = t

    def list_templates(self) -> List[AnswerTemplate]:
        return list(self._templates)

    def get_template(self, template_id: str) -> Optional[AnswerTemplate]:
        return self._index.get(template_id)

    def search_by_name(self, name: str) -> Optional[AnswerTemplate]:
        for t in self._templates:
            if name in t.template_name or name in t.template_id:
                return t
        return None

